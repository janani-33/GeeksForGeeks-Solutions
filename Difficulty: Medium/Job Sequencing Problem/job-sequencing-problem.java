class Solution
{
    //Function to find the maximum profit and the number of jobs done.
    int[] JobScheduling(Job arr[], int n)
    {
        Arrays.sort(arr,(x,y)->y.profit-x.profit);
        int max=0;
        for(Job j:arr) 
        max=Math.max(max,j.deadline);
        int d[]=new int[max+1];
        Arrays.fill(d,-1);
        int mprofit=0,tot=0;
        for(Job j:arr){
            for(int i=j.deadline;i>0;i--){
                if(d[i]==-1){
                    d[i]=j.profit;
                    mprofit+=j.profit;
                    tot++;
                    break;
                }
            }
        }
        return new int[]{tot,mprofit};
    }
}

